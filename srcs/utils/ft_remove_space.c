#include "minishell.h"

char	*ft_remove_space(char *str)
{
	int	i;

	i = 0;
	while (*str && (*str == ' '))
		str++;
	while (str[i] && str[i] != ' ')
		i++;
	return (ft_strndup(str, i));
}
