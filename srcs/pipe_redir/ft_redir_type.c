int		ft_redir_type(char c, char d, int *type)
{
	if (c == '>')
		*type = 0;
	else if (c == '>' && d == '>')
		*type = 1;
	else if (c == '<')
		*type = 2;
	else
		*type = -1;
	return (0);
}
