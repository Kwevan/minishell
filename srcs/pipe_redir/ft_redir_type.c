int		ft_redir_type(char c, char d, int *type, int *i)
{
	if (!d)
		return (0);
	if (c == '>' && d == '>')
	{
		*type = 1;
		*i += 1;
	}
	else if (c == '>')
		*type = 0;
	else if (c == '<')
		*type = 2;
	else
		*type = -1;
	return (0);
}
